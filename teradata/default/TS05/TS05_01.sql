CREATE  PROCEDURE TS05_01.UPDATE_TAB_EMP1()
 BEGIN
 
UPDATE TS05_01.UPD_DEPT1 
SET EMP_ID=(SELECT UPD_EMP3.EMP_ID FROM TS05_01.UPD_EMP3 ,TS05_01.UPD_EMP4 
WHERE UPD_EMP3.EMP_ID=UPD_EMP4.EMP_ID)
WHERE UPD_DEPT1.EMP_NAME=(SELECT UPD_EMP3.EMP_NAME FROM TS05_01.UPD_EMP3 ,TS05_01.UPD_EMP4 
WHERE UPD_EMP3.EMP_NAME=UPD_EMP4.EMP_NAME);

END;